<?php

namespace derhasi\upmkTournament;

class Contestant implements ItemInterface
{

    /**
     * @var string
     */
    protected $name;

    /**
     * @param string $name
     */
    public function __construct($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName() {
        return $this->name;
    }

    /**
     * @return string
     */
    public function getId() {
        return $this->name;
    }

}
